public class Main {
    public static void main(String[] args) {
        short max = 32767;
        short min = -32768;

        System.out.println("Max: "+ max);
        System.out.println("Max + 1: "+ (++max));
        System.out.println("Min: "+ (min));
        System.out.println("Min - 1: "+ (--min));




    }
}